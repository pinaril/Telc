package com.telc.domain.Emtity;

public class Periodic  {
  
	private String periodic_id;
	private String period;
	private String period_detail;
	private String user_id;
	private String content;
	private int priority;
	private int iscompeleted;
	public String getPeriodic_id() {
		return periodic_id;
	}
	public void setPeriodic_id(String periodic_id) {
		this.periodic_id = periodic_id;
	}
	public String getPeriod() {
		return period;
	}
	public void setPeriod(String period) {
		this.period = period;
	}
	public String getPeriod_detail() {
		return period_detail;
	}
	public void setPeriod_detail(String period_detail) {
		this.period_detail = period_detail;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getPriority() {
		return priority;
	}
	public void setPriority(int priority) {
		this.priority = priority;
	}
	public int getIscompeleted() {
		return iscompeleted;
	}
	public void setIscompeleted(int iscompeleted) {
		this.iscompeleted = iscompeleted;
	}

	
	
}
